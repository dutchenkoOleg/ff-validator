import { isValid, IValidFn } from '../../core';

// -----------------------------------------------------------------------------
// Rule
// -----------------------------------------------------------------------------

export const urlDomain = (errorMessage: string): IValidFn => (value, allValues) =>
	isValid(
		/^(?:https?:\/\/|\/\/)[a-z0-9-][a-z0-9-.@:]*\.[a-z]{2,}/i.test(value),
		errorMessage
	);

import { isValid, IValidFn } from '../../core';

// -----------------------------------------------------------------------------
// Rule
// -----------------------------------------------------------------------------

export const emailDomain = (errorMessage: string): IValidFn => (value, allValues) =>
	isValid(/(@[a-z0-9-][a-z0-9-.]*\.[a-z]{2,}$)/.test(value), errorMessage);

import { isValid, IValidFn } from '../../core';

// -----------------------------------------------------------------------------
// Rule
// -----------------------------------------------------------------------------

export const urlCharacters = (errorMessage: string): IValidFn => (value, allValues) =>
	isValid(/^[A-Za-z0-9_.~\-:\/?#\[\]\%@!$&'()*+,;=]+$/.test(value), errorMessage);
